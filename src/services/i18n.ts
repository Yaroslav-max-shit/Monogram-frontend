let currentLanguage: Language = 'ru';

const languageListeners: Array<() => void> = [];

export function onLanguageChange(callback: () => void): () => void {
  languageListeners.push(callback);
  return () => {
    const idx = languageListeners.indexOf(callback);
    if (idx > -1) languageListeners.splice(idx, 1);
  };
}

export function setLanguage(lang: Language): void {
  currentLanguage = lang;
  localStorage.setItem('language', lang);
  languageListeners.forEach(cb => cb());
}

export function getLanguage(): Language {
  const saved = localStorage.getItem('language') as Language;
  if (saved && translations[saved]) {
    currentLanguage = saved;
  }
  return currentLanguage;
}

export function t(key: string): string {
  const lang = getLanguage();
  return translations[lang]?.[key] || translations.ru[key] || key;
}

export default { t, setLanguage, getLanguage };